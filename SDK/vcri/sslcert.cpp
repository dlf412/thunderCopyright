static CURLcode load_cert(const char* mypem,void * sslctx) {
    X509_STORE * store = NULL;
    X509 * cert = NULL;
    BIO * bio = NULL;

    bio = BIO_new_mem_buf((void*)mypem, -1);
    if (bio == NULL)
    {
        return CURLE_SSL_CACERT;
    }

    /* use it to read the PEM formatted certificate from memory into an X509
    * structure that SSL can use
    */ 
    PEM_read_bio_X509(bio, &cert, 0, NULL);
    if (cert == NULL)
    {
        BIO_free(bio);
        return CURLE_SSL_CACERT;
    }

    /* get a pointer to the X509 certificate store (which may be empty!) */ 
    store = SSL_CTX_get_cert_store((SSL_CTX*)sslctx);
    if (store == NULL)
    {
        X509_free(cert);
        BIO_free(bio);
        return CURLE_SSL_CACERT;
    }

    /* add our certificate to this store */ 
    if (X509_STORE_add_cert(store, cert)==0)
    {
        X509_free(cert);
        BIO_free(bio);
        return CURLE_SSL_CACERT;
    }

    /* decrease reference counts */ 
    X509_free(cert);
    BIO_free(bio);

    return CURLE_OK;
}

CURLcode sslctx_function(CURL * curl, void * sslctx, void * parm) {
    CURLcode ret; 
    ret = load_cert(cert_pem1,sslctx);
    if (ret != CURLE_OK)
    {
        return CURLE_SSL_CACERT;
    }

    ret = load_cert(cert_pem2,sslctx);
    if (ret != CURLE_OK)
    {
        return CURLE_SSL_CACERT;
    }

    ret = load_cert(cert_pem3,sslctx);
    if (ret != CURLE_OK)
    {
        return CURLE_SSL_CACERT;
    }

    return CURLE_OK;
}
